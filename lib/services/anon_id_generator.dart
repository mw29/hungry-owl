String getAnonId(String uid) {
  final color = colorNames[Random().nextInt(colorNames.length)].toLowerCase();
  final animal =
      animalNames[Random().nextInt(animalNames.length)].toLowerCase();
  final hash = uid.substring(0, 6);
  return "$color-$animal-$hash";
}

